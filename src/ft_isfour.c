#include "fillit.h"

int	ft_isfour(char *file)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (file[i])
	{
		if (file[i] == '#')
			c++;
		if (file[i] == '\n' && (file[i + 1] == '\n' || !(file[i + 1])))
		{
			if (c != 4)
				return (0);
			c = 0;
		}
		i++;
	}
	return (1);
}
