#include "fillit.h"

char		*ft_trimvert(char *bloc)
{
	int			i;

	while (bloc[0] != '#' && bloc[4] != '#' && bloc[8] != '#' &&
	bloc[12] != '#')
	{
		i = 0;
		while (bloc[i])
		{
			if (bloc[i] == '#')
			{
				bloc[i - 1] = '#';
				bloc[i] = '.';
			}
			i++;
		}
	}
	return (bloc);
}
